void printingArithematicExpression(itNode *ptr){
	if(ptr == null){
		return;
	}
	if(ptr->left == null && ptr->right == null){
		printf("%d",ptr->value);
		return;
	}
	printf("(");
	printingArithematicExpression(ptr->left);
	printf("%c",ptr->value);
	printingArithematicExpression(ptr->right);
	printf(")");
}

