/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
unsigned int heightOfNode(itNode *ptr){
	if(ptr == null){
		return 0;
	}
	return max(heightOfNode(ptr->left),heightOfNode(ptr->right)) + 1;
}

