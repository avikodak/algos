/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
string getRunLengthString(string userInput){
	string result;
	if(userInput.size() == 0){
		return result;
	}
	unsigned int counter = 0,frequency;
	while(counter < userInput.size()){
		frequency = 0;
		while(counter+1 < userInput.size() && userInput[counter] == userInput[counter+1]){
			frequency++;
		}
		result.append(userInput[counter]);
		result.append(atoi(frequency));
		counter++;
	}
	return result;
}

