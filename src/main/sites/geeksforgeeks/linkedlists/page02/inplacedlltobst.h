/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
dillNode *inPlaceSortedDLLToBST(dillNode **ptr,int size){
	if(size <= 0){
		return null;
	}
	dillNode *left = inPlaceSortedDLLToBST(ptr,size/2);
	dillNode *root = *ptr;
	root->prev = left;
	(*ptr) = (*ptr)->next;
	root->next = inPlaceSortedDLLToBST(ptr,size - size/2-1);
	return root;

}

