bool areTreesIsomorphs(itNode *firstPtr,itNode *secondPtr){
	if(firstPtr == null && secondPtr == null){
		return true;
	}
	if(firstPtr == null || secondPtr == null){
		return false;
	}
	if(firstPtr->value != secondPtr->value){
		return false;
	}
	return (areTreesIsomorphs(firstPtr->left,secondPtr->left) && areTreesIsomorphs(firstPtr->right,secondPtr->right))||(areTreesIsomorphs(firstPtr->left,secondPtr->right) && areTreesIsomorphs(firstPtr->right,secondPtr->left));
}

