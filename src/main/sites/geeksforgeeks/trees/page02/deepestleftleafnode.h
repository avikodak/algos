/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
void getDeepestLeftLeafNode(itNode *ptr,itNode **deepestLeftLeaf,unsigned int &maxLevel,unsigned int currentLevel){
	if(ptr == null){
		return;
	}
	if(ptr->left == null && ptr->right == null){
		if(maxLevel < currentLevel){
			*deepestLeftLeaf = ptr;
			maxLevel = currentLevel;
		}
		return;
	}
	getDeepestLeftLeafNode(ptr->left,deepestLeftLeaf,maxLevel,currentLevel+1);
	getDeepestLeftLeafNode(ptr->right,deepestLeftLeaf,maxLevel,currentLevel+1);
}

