/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
bool removeAllNodesNotInPathGreaterThanK(itNode *ptr,int key,int runningSum = 0){
	if(ptr == null){
		return false;
	}
	if(ptr->left == null && ptr->right == null){
		if(runningSum + ptr->value != key){
			free(ptr);
		}
		return runningSum + ptr->value == key;
	}
	bool leftTruthValue = removeAllNodesNotInPathGreaterThanK(ptr->left,key,runningSum+ptr->value);
	bool rightTruthValue = removeAllNodesNotInPathGreaterThanK(ptr->right,key,runningSum+ptr->value);
	if(!leftTruthValue){
		ptr->left = null;
	}
	if(!rightTruthValue){
		ptr->right = null;
	}
	if(!leftTruthValue && !rightTruthValue){
		free(ptr);
	}
	return leftTruthValue || rightTruthValue;
}


