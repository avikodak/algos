/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
unsigned int sumOfAllNumbersRootToLeaf(itNode *ptr,unsigned int runningNumber){
	if(ptr == null){
		return 0;
	}
	if(ptr->left == null && ptr->right == null){
		return runningNumber*10 + ptr->value;
	}
	return sumOfAllNumbersRootToLeaf(ptr->left,runningNumber*10+ptr->value) + sumOfAllNumbersRootToLeaf(ptr->right,runningNumber*10+ptr->value);
}

