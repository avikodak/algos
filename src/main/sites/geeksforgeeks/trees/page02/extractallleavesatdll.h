/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
bool extractLeavesAsDLL(itNode *ptr,itNode **headOfDll){
	if(ptr == null){
		return false;
	}
	static itNode *tailDll = null;
	if(ptr->left == null && ptr->right == null){
		if(tailDll == null){
			*headOfDll = ptr;
			tailDll = ptr;
		}else{
			tailDll->right = ptr;
			ptr->left = tailDll;
		}
		return true;
	}
	bool isLeftNodeLeaf = extractLeavesAsDLL(ptr->left,headOfDll);
	if(isLeftNodeLeaf){
		ptr->left = null;
	}
	bool isRightNodeLeaf = extractLeavesAsDLL(ptr->right,headOfDll);
	if(isRightNodeLeaf){
		ptr->right = null;
	}
	return false;
}

