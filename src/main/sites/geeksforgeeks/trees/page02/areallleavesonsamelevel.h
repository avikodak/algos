/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
bool areAllLeavesAtSameLevel(itNode *ptr,unsigned int currentLevel){
	if(ptr == null){
		return true;
	}
	static unsigned int lastLevelLeaf = UINT_MAX;
	if(ptr->left == null && ptr->right == null){
		if(lastLevelLeaf == UINT_MAX){
			lastLevelLeaf = currentLevel;
			return true;
		}
		return lastLevelLeaf == currentLevel?true:false;
	}
	return areAllLeavesAtSameLevel(ptr->left,currentLevel+1) && areAllLeavesAtSameLevel(ptr->right,currentLevel+1);
}


