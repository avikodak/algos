/****************************************************************************************************************************************************/
/* 																	O(N) Algorithm 																    */
/****************************************************************************************************************************************************/
void sortedOrderBSTArray(vector<int> sortedBST,int index){
	if(sortedBST.size() == 0 || index < 0 || index >= sortedBST.size()){
		return;
	}
	sortedOrderBSTArray(sortedBST,2*index+1);
	printf("%d\t",sortedBST[index]);
	sortedOrderBSTArray(sortedBST,2*index+2);
}

/****************************************************************************************************************************************************/
/* 																O(NLOGN) Algorithm 																    */
/****************************************************************************************************************************************************/
void sortedOrderBSTArrayONLOGN(vector<int> sortedBST){
	if(sortedBST.size() == 0){
		return;
	}
	sort(sortedBST.begin(),sortedBST.end());
	for(unsigned int counter = 0;counter < sortedBST.size();counter++){
		printf("%d\t",sortedBST[counter]);
	}
}

